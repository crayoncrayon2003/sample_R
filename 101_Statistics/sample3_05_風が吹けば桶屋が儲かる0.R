  cat("「風が吹けば桶屋が儲かる」は、\n")
  cat("一見すると無関係に見える出来事が、\n")
  cat("複数の中間要因を経て最終的な結果につながることを表す日本のことわざです。\n\n")

  cat("このことわざは、直接的な因果関係だけでなく、\n")
  cat("間接的・連鎖的な因果関係を考える重要性を示しています。\n\n")

  cat("一般的に説明される因果連鎖の一例は次の通りです。\n")
